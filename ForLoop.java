package com.uno;

public class ForLoop {

	public static void main(String[] args) {
		int lastnumber = 10;
		int sum = 0;
		
		
  for( int startingnumber = 1;startingnumber <=lastnumber; startingnumber++) {
	  sum = sum + startingnumber;}
  System.out.println("\n sum =" + sum);
  }
	}

